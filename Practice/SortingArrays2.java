public class SortingArrays2 {
        public static void main(String[] args) {

                double [] array = {85.5, 92.0, 78.3, 95.7};
                  double result = 0;
                  for (int count = 0; count < array.length; count++) {
                       result += array[count];
              }
                   System.out.println("The sum of elements in the array is: " + result);
        }


}
